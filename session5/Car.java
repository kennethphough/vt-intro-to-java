package session5;

public class Car {
    // Instance variables
    String color;
    String model;
    int year;

    // Method to start the engine
    void startEngine() {
        System.out.println("The " + color + " " + model + " engine started.");
    }

    // Method to stop the engine
    void stopEngine() {
        System.out.println("The " + color + " " + model + " engine stopped.");
    }
}
